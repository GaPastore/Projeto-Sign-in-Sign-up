    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        String nomeUsuario, emailUsuario, senhaUsuario;
        nomeUsuario = NomeCadastro.getText();
        emailUsuario = EmailCadastro.getText();
        senhaUsuario = SenhaCadastro.getText();
        UsuarioDTO objUsuarioDTO = new UsuarioDTO();
        objUsuarioDTO.setNomeUsuario(nomeUsuario);
        if(UsuarioDTO.validarEmail(emailUsuario)){
            objUsuarioDTO.setEmailUsuario(emailUsuario);   
        }
        objUsuarioDTO.setSenhaUsuario(senhaUsuario);
        UsuarioDAO objUsuarioDAO = new UsuarioDAO();
        int resultUsuarioDAO = objUsuarioDAO.cadastrarUsuario(objUsuarioDTO);
        if(resultUsuarioDAO != 0 && resultUsuarioDAO != 1){
            //tela
            formLoginView objFormLoginView = new formLoginView();
            objFormLoginView.setVisible(true);
            dispose();
            
        }else if(resultUsuarioDAO == 0){
            //incorreto
            JOptionPane.showMessageDialog(null, "Campos n√£o preenchidos!");
        }
        
    }//GEN-LAST:event_BtnCadastrarActionPerformed

